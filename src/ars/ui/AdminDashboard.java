    private void jButton9ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton9ActionPerformed
        // TODO add your handling code here:
        int id = Integer.parseInt(idField2.getText());
       UsersDAO dao = new UsersDAO();
       int choice = JOptionPane.showConfirmDialog(null, "Are you sure you want to remove this user?","Confirm User Removal", JOptionPane.YES_NO_OPTION,JOptionPane.QUESTION_MESSAGE);
        if(choice == JOptionPane.YES_OPTION){
        try {
            boolean state = dao.deleteUser(id);
            if(state){
                JOptionPane.showMessageDialog(null,"User deleted successfully");
            idField2.setText("");
            refreshUsersTable();
            }
            else{
                JOptionPane.showMessageDialog(null,"Failed to delete user");
            }
        }catch (SQLException ex) {
           ex.printStackTrace();
        }
        }
    }//GEN-LAST:event_jButton9ActionPerformed

