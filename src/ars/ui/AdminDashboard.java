    private void jButton5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton5ActionPerformed
        // TODO add your handling code here:
        String origin = originField.getText();
        String destination = destField.getText();
        String departure_time = depField.getText();
        String arrival_time = arrivalField.getText();
        String status = statusCombo.getSelectedItem().toString();
        FlightsDAO dao = new FlightsDAO();
        try {
            boolean state = dao.addFlight(origin, destination, departure_time, arrival_time, status);
            if(state){
                JOptionPane.showMessageDialog(null,"Flight added successfully");
                originField.setText("");
                destField.setText("");
                depField.setText("");
                arrivalField.setText("");
                statusCombo.setSelectedIndex(0);
                refreshFlightsTable();
            }
            else{JOptionPane.showMessageDialog(null,"Failed to add new flight");}
        } catch (SQLException ex) {
            ex.printStackTrace();
        }
    }//GEN-LAST:event_jButton5ActionPerformed

    private void cityComboActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cityComboActionPerformed
        // TODO add your handling code here:
        DefaultTableModel model = (DefaultTableModel)flightsTable.getModel();
        TableRowSorter<DefaultTableModel>sorter = new TableRowSorter<>(model);
        flightsTable.setRowSorter(sorter);
        String selectedCity = cityCombo.getSelectedItem().toString();
        if(selectedCity.equals("All")){
            sorter.setRowFilter(null);
        }else{
            sorter.setRowFilter(RowFilter.regexFilter("(?i)"+selectedCity,1,2));
        }
    }//GEN-LAST:event_cityComboActionPerformed

