    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        int choice = JOptionPane.showConfirmDialog(null, "Do you wish to book this flight?","Confirm Ticket Purchase", JOptionPane.YES_NO_OPTION,JOptionPane.QUESTION_MESSAGE);
        if(choice == JOptionPane.YES_OPTION){
            JFileChooser fileChooser = new JFileChooser();
            fileChooser.setDialogTitle("Save Flight Receipt");
            int userSelection = fileChooser.showSaveDialog(null);
            if(userSelection == JFileChooser.APPROVE_OPTION){
                File fileToSave = fileChooser.getSelectedFile();
                try(BufferedWriter writer = new BufferedWriter(new FileWriter(fileToSave))){
                    writer.write("ARS - Flight Receipt \n\n");
                    DefaultTableModel model = (DefaultTableModel) userFlightsTable.getModel();
                    int selectedRow = userFlightsTable.getSelectedRow();
                    if(selectedRow != -1){
                        writer.write("Flight ID: " + model.getValueAt(selectedRow,0)+"\n");
                        writer.write("Origin: " + model.getValueAt(selectedRow,1)+"\n");
                        writer.write("Destination: " + model.getValueAt(selectedRow,2)+"\n");
                        writer.write("Departure Time: " + model.getValueAt(selectedRow,3)+"\n");
                        writer.write("Arrival Time: " + model.getValueAt(selectedRow,4)+"\n");
                        writer.write("Flight Status: " + model.getValueAt(selectedRow,5)+"\n\n");
                    
                    writer.write("Thank you for flying with ARS!");
                    JOptionPane.showMessageDialog(null,"Receipt saved successfully!");}
                    else{
                        JOptionPane.showMessageDialog(null,"Please select a flight row first");
                    }
                } catch (IOException ex) {
                    ex.printStackTrace();
                }
            }
        }
    }//GEN-LAST:event_jButton1ActionPerformed

    private void userComboActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_userComboActionPerformed
        // TODO add your handling code here:
        DefaultTableModel model = (DefaultTableModel)userFlightsTable.getModel();
        TableRowSorter<DefaultTableModel>sorter = new TableRowSorter<>(model);
        userFlightsTable.setRowSorter(sorter);
        String selectedCity = userCombo.getSelectedItem().toString();
        if(selectedCity.equals("All")){
            sorter.setRowFilter(null);
        }else{
            sorter.setRowFilter(RowFilter.regexFilter("(?i)"+selectedCity,1,2));
        }
    }//GEN-LAST:event_userComboActionPerformed

