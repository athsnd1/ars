    public void refreshFlightsTable(){
            try{
            Connection conn = DBHelper.getConnection();
            String sql = "SELECT id, origin, destination, departure_time,arrival_time, status FROM flights";
            PreparedStatement stmt = conn.prepareStatement(sql);
            ResultSet rs = stmt.executeQuery();
            DefaultTableModel model = (DefaultTableModel)userFlightsTable.getModel();
            model.setRowCount(0); 
            while(rs.next()){
                Object[] row = {rs.getInt("id"),rs.getString("origin"),rs.getString("destination"),rs.getString("departure_time"),rs.getString("arrival_time"),rs.getString("status")};
                model.addRow(row);} 
            rs.close();
            stmt.close();
            conn.close();
            }catch(SQLException e){
                    e.printStackTrace();
                }
        }

